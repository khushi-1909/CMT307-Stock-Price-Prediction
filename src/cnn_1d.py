def make_1d_cnn(n_features,optimiser='adam', dropout_rate=0.2,  kernel_size = 3, padding = 'same') :
  model  = Sequential()
  model.add(Conv1D(filters=32, kernel_size=kernel_size, padding =padding,input_shape = (n_features,1)))
  model.add(BatchNormalization())
  model.add(ReLU())
  model.add(Dropout(rate=dropout_rate))
  model.add(Conv1D(filters=64, kernel_size=kernel_size, padding =padding))
  model.add(BatchNormalization())
  model.add(ReLU())
  model.add(Dropout(rate=dropout_rate))
  model.add(Conv1D(filters=128, kernel_size=kernel_size, padding =padding))
  model.add(MaxPooling1D(pool_size=2))
  model.add(Flatten())
  model.add(Dense(units=100, activation='relu'))
  model.add(Dense(units=50, activation='relu'))
  model.add(Dense(units=1, activation = 'sigmoid'))

  model.compile(loss=BinaryFocalCrossentropy(from_logits=False), optimizer='adam', 
                metrics = ['accuracy', AUC(name='auc')])
  #model.summary()

  return model

