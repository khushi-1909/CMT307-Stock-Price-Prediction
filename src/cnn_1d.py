def predict_90(train, test, predictors, model, engine = 'sklearn'):


    model.fit(train[predictors], train["Target"],  shuffle=True, epochs=20, batch_size=30)
    if engine == 'keras':
        probs = model.predict(test[predictors])[:,0]
    else: 
        probs = model.predict_proba(test[predictors])[:,1]
    probs = pd.Series(probs, index=test.index) # Convert numpy array to pandas Series
    preds = (probs > 0.51).astype(int)
    preds = pd.Series(preds, index=test.index)
    combined = pd.concat({"Target": test["Target"],"Predictions": preds, 'Probabilities': probs}, axis=1)
    return combined

def backtest_90(data, model, predictors, start=2500, step=360, engine = 'sklearn'):
    data = data.copy()
    all_predictions = []
    oob_scores = []

    for i in range(start, data.shape[0], step):
        train = data.iloc[0:i].copy()
        test = data.iloc[i:(i+step)].copy()
        predictions = predict_90(train, test, predictors, model, engine = engine)

        all_predictions.append(predictions)

        if hasattr(model, "oob_score_"):
            oob_scores.append(model.oob_score_)

    return pd.concat(all_predictions), oob_scores


def make_target(ticker_data):
  ticker_data = ticker_data[['High', 'Low', 'Open', 'Close', 'Volume']]
  ticker_data['close_90'] = ticker_data['Close'].shift(-90)
  ticker_data['target'] = (ticker_data['close_90'] > ticker_data['Close']).astype(int)
  #ticker_data['Close'] = np.log(ticker_data['Close'])
  return ticker_data

def weighted_ma(t, close, window_size):
  numer_sum = 0
  denom_sum = 0
  for i in range(0, window_size-1, 1):
    numer_sum += ((window_size -i) * close.iloc[t-i])
    denom_sum += (window_size-i)
  return numer_sum / denom_sum

def exponential_ma(close, window_size, smoothing_alpha=0.3):
  smooth = np.zeros(window_size)
  smooth[0] = close.iloc[0]
  for i in range(1,window_size):
    smooth[i] = (smoothing_alpha * close.iloc[i]) + ((1-smoothing_alpha) * smooth[i-1])
  return smooth
